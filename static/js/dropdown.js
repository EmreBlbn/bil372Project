
function PatientDropdown() {
    document.getElementById('patient_dropdown').style.display = "none";
    document.getElementById('patient_dropclose').style.display = "block";
    document.getElementById('pdb').style.display = "block";
}

function PatientDropclose() {
    document.getElementById('patient_dropdown').style.display = "block";
    document.getElementById('patient_dropclose').style.display = "none";
    document.getElementById('pdb').style.display = "none";
}

